package com.cosine.cosgame.marshbros;

public class RoleEntity {
	String img;
	String hp;
	String atk;
	String canAttack;
	String choice;
	String attackTarget;
	String hasAction;
	String actionText;
	String actionType;
	
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getHp() {
		return hp;
	}
	public void setHp(String hp) {
		this.hp = hp;
	}
	public String getAtk() {
		return atk;
	}
	public void setAtk(String atk) {
		this.atk = atk;
	}
	public String getCanAttack() {
		return canAttack;
	}
	public void setCanAttack(String canAttack) {
		this.canAttack = canAttack;
	}
	public String getChoice() {
		return choice;
	}
	public void setChoice(String choice) {
		this.choice = choice;
	}
	public String getAttackTarget() {
		return attackTarget;
	}
	public void setAttackTarget(String attackTarget) {
		this.attackTarget = attackTarget;
	}
	public String getHasAction() {
		return hasAction;
	}
	public void setHasAction(String hasAction) {
		this.hasAction = hasAction;
	}
	public String getActionText() {
		return actionText;
	}
	public void setActionText(String actionText) {
		this.actionText = actionText;
	}
	public String getActionType() {
		return actionType;
	}
	public void setActionType(String actionType) {
		this.actionType = actionType;
	}
	
	
}
